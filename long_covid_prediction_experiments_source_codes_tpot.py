def tpot_clf(X_train, y_train, gen, X_test, y_test):
  #find the best clf
  tpot = TPOTClassifier(generations=gen, population_size=50, verbosity=2, scoring='f1_micro', max_eval_time_mins=3, random_state=10) #pop size = Number of individuals to retain in the GP
  tpot.fit(X_train, y_train)
  tpot.export('tpot_pipeline.py')

  y_pred = tpot.predict(X_test)
  # Calculate metrics
  p = precision_score(y_test, y_pred, average='weighted', zero_division=0)
  r = recall_score(y_test, y_pred, average='weighted', zero_division=0)
  a = accuracy_score(y_test, y_pred)

  print(f'Precision: {p:.2%}')
  print(f'Recall: {r:.2%}')
  print(f'Accuracy: {a:.2%}')
  print()

  return p, r, a

